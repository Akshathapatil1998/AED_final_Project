    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        if(!addUser.getText().equals("") && addPwd.getPassword().length!=0)
        {
        
        for(userCred ucd: userData.getUserData())
        {
           if(addUser.getText().toLowerCase().equals(ucd.getUserName().toLowerCase()))
           {
               
               System.out.println(ucd.getUserName().toLowerCase());
               JOptionPane.showMessageDialog(this, "user already exists");
               
              return;
               
           }
        }
           
        userCred uc= userData.addDetails();
        
        uc.setUserName(addUser.getText().toLowerCase());
        uc.setPwd(new String(addPwd.getPassword()));
        
        
        uc.setUserType(selEnt.getSelectedItem().toString());
        //patient pat = patData.addDetails("pratik", UID, 23, "Male",  454545454, "car@gmail.com");
        
        
        populateusercred();
           
        
        }
            
        
        
        else{
            
            JOptionPane.showMessageDialog(this, "Please enter Valid credentials ");
        return;
        }
    }//GEN-LAST:event_registerActionPerformed

