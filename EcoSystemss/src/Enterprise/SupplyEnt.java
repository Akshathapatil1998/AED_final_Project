    private void populateusercred() {
        DefaultTableModel model = (DefaultTableModel) entUser.getModel();
        
        model.setRowCount(0);
        for(userCred em : userData.getUserData())
        {
            
            
        Object[] row = new Object[10];
        row[0]=em.getUserName();
        row[1]=em.getUserType();
        
        
        model.addRow(row);
            
        
        }
    }
